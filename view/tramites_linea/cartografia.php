	<h2 class="sub-title">Cartografía</h2>
	<p style="text-align:justify">Jesús María es un municipio en crecimiento y transformación, al formar parte de la zona metropolitana y por consiguiente el surgimiento de una serie de retos en materia de desarrollo a raíz de la expansión que se ha dado en los últimos 20 años.</p>

	<p style="text-align:justify">Con el objetivo de documentar y difundir los instrumentos de planeación urbana se visualizó la realización del Programa de Desarrollo Urbano de la Ciudad de Jesús María 2015 -2035, Programa Subregional de Desarrollo Urbano de Tepetates 2015 -2035,Programa Subregional de Desarrollo Urbano de J. Gómez Portugal 2015 -2035, Programa Subregional de Desarrollo Urbano La Tomatina2015 -2035, Programa Subregional de Desarrollo Urbano Ignacio Zaragoza-Buenavista 2015 -2035 y el Esquema de Desarrollo Urbano de la Localidad de los Arquitos 2015 -2035,los cuales garantizaran que el crecimiento y transformación de nuestro municipio, el cual se deberá llevar a cabo de una forma organizada y estructurada, cabe resaltar que dicho instrumento de planeación fue también consensado con la ciudadanía buscando siempre mejorar el entorno y calidad de vida de los Jesusmarienses.</p>

	<p style="text-align:justify">Cabe resaltar que la elaboración los Programas de planeación fueron elaborados con una visión 2015-2035, determinando así la prioridad de contar con instrumentos técnico-jurídicos para dar cumplimiento a los objetivos y metas plasmadas en el Plan Municipal de Desarrollo 2014-2016, posicionado en un peldaño competitivo, innovador y a la vanguardia en materia de planeación y desarrollo urbano.</p>

	<p style="text-align:justify">El impulso a la realización del documento fue bajo la visión de promover la inversión y construcción de obra pública, la definición de los usos y destinos de suelo, el respeto al medio ambiente, el patrimonio natural y cultural, así como la identidad de las distintas costumbres de los habitantes.</p>

