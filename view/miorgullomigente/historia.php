	<h2 class="sub-title">Historia</h2>
	<br>
	<div>
	<div style="text-align: justify;"><span><strong>Pa&iacute;s:</strong> M&eacute;xico</span></div>

	<div style="text-align: justify;"><span><strong>Estado:</strong> Aguascalientes</span></div>

	<div style="text-align: justify;"><span><strong>Municipio:&nbsp;</strong>Jes&uacute;s Maria</span></div>

	<div style="text-align: justify;"><span><strong>Superficie:</strong>&nbsp;493.10km2</span></div>

	<div style="text-align: justify;"><span><strong>Sitio Web:</strong>&nbsp;<u>www.jesusmaria.gob.mx</u></span></div>

	<p style="text-align:justify"><br />
	<span>Es un municipio mexicano de la Regi&oacute;n central del pa&iacute;s el cual esta formado por &nbsp;comunidades y rancher&iacute;as &nbsp;dentro de su territorio municipal y se organizan pol&iacute;ticamente en delegaciones y comisar&iacute;as.</span></p>

	<p style="text-align:justify"><span>El 1&ordm; de Diciembre de 1701, Diego De Parga y Galloso abogado de la Real Audiencia, dio posesi&oacute;n de la tierra a los naturales; &egrave;sta se considera la fecha oficial de la fundaci&oacute;n &ldquo;XONACATIQUE&rdquo; hoy JES&Uacute;S MAR&Iacute;A.</span></p>

	<p style="text-align:justify"><span>En 1765 se erige en VILLA con el nombre de &ldquo;Jes&uacute;s Mar&iacute;a de los Dolores&rdquo; y despu&eacute;s de la Revoluci&oacute;n es cuando su nombre se acorta quedando como actualmente se conserva &ldquo;JES&Uacute;S MAR&Iacute;A&rdquo;.</span></p>

	<p style="text-align:justify"><span><strong>Geograf&iacute;a</strong></span></p>

	<p style="text-align:justify"><span>El territorio del municipio de Jes&uacute;s Mar&iacute;a, cuenta con una superficie total de 493.10 kil&oacute;metros cuadrados el cual representa el 8.9% de la superficie total del Estado de Aguascalientes.</span></p>

	<p style="text-align:justify"><span><strong>Municipios Lim&iacute;trofes</strong></span></p>

	<p style="text-align:justify"><span>Jes&uacute;s Mar&iacute;a se localiza al Centro-Oeste del Estado de Aguascalientes, colinda al norte con, los municipios de San Jos&eacute; de Gracia y Pabell&oacute;n de Arteaga; al este con los Municipios de San Francisco de los Romo y Aguascalientes; al sur con los Municipios de Aguascalientes y Calvillo y al oeste con los Municipios de Calvillo y San Jos&eacute; de Gracia&nbsp;<em>(fuente: INEGI Marco Geoestad&igrave;stico 1995).</em></span></p>

	<p style="text-align:justify"><span><strong>Gastronom&iacute;a</strong></span></p>

	<p style="text-align:justify"><span>La gastronom&iacute;a en Jes&uacute;s Mar&iacute;a destaca ante todo por su sencillez, riqueza y variedad del producto; de los platillos t&iacute;picos en general destacan las carnitas al estilo Jes&uacute;s Maria, las gorditas picadas en sus diversas variedades, tacos dorados, enchiladas, pozole, tamales y condoches.</span></p>

	<p style="text-align:justify"><span><strong><span style="color:#000000">Actividades Econ&oacute;micas</span></strong></span></p>

	<p style="text-align:justify"><span>Es un pueblo dedicado a:</span></p>

	<p style="text-align:justify"><span><strong>Agricultura:</strong>&nbsp;Ma&iacute;z, fr&iacute;jol, forrajes y hortalizas.</span></p>

	<p style="text-align:justify"><span><strong>Ganader&iacute;a:</strong>&nbsp;bovinos, porcinos, equinos y aves.</span></p>

	<p style="text-align:justify"><span><strong>Industria:</strong>&nbsp;mueblera, textil, estructuras met&aacute;licas, mec&aacute;nica, l&aacute;cteos, maderera, pl&aacute;stico, herramientas, alimenticia, autopartes automotriz, electr&oacute;nica, entre otras.</span></p>

	<p style="text-align:justify"><span><strong>Artesanal:&nbsp;</strong>Talla en Madera, Talla en Cantera, Cer&aacute;mica, Alfarer&iacute;a, bordada, deshilada y tejida.</span></p>

	<p style="text-align:justify"><span><strong>Industria Mueblera:&nbsp;</strong>Jes&uacute;s Mar&iacute;a a ganado merecida fama como fabricante de muebles finos de estilo europeo, provenzal gracias a la iniciativa de Alejandro De La Cruz Saucedo, emprendedor que dio inicio hace mas de 20 a&ntilde;os esta industria, que hoy en d&iacute;a, sostiene la econom&iacute;a de cientos de habitantes, en numerosas fabricas grandes&nbsp; y peque&ntilde;as y cuyo producto traspasa las fronteras estatales y nacionales.<br />
	Merecen especial menci&oacute;n los artesanos, talladores de maderas finas quienes dan el toque final que adorna las obras: de lujosos comedores salas y consoletas.</span></p>
