<h2 class="sub-title">Escudo</h2>
<br>
<div>
<p style="text-align:justify">El nombre y el Escudo del Municipio son el signo de identidad y s&iacute;mbolo representativo del Municipio, respectivamente. El Municipio conserva su nombre actual de &ldquo;Jes&uacute;s Mar&iacute;a&rdquo; el cual no podr&aacute; ser cambiado, sino por acuerdo un&aacute;nime del Ayuntamiento y con la aprobaci&oacute;n de la Legislatura del Estado. La descripci&oacute;n del escudo del Municipio de Jes&uacute;s Mar&iacute;a, las figuras representadas en los diferentes cuadros, son la Tradici&oacute;n, Industria, Ganader&iacute;a y Agricultura:</p>

<p style="text-align:justify"><span style="color:#000000">1. Tradici&oacute;n:</span> El cuadrante superior izquierdo representa el vestuario tradicional de los hombres que acompa&ntilde;aron a Santo Santiago.</p>

<p style="text-align:justify"><span style="color:#000000">2. Religi&oacute;n:</span> El cuadrante superior derecho representa en s&iacute; la religi&oacute;n por medio de la corona de Jesucristo Y el estandarte del Santo Santiago, que representa las festividades que se celebran en esta ciudad.</p>

<p style="text-align:justify"><span style="color:#000000">3. Ganader&iacute;a:</span> En el c&iacute;rculo central la figura representa la actividad ganadera de la regi&oacute;n.</p>

<p style="text-align:justify"><span style="color:#000000">4. Industria:</span> En el cuadrante inferior izquierdo la figura representa la actividad econ&oacute;mica industrial de la regi&oacute;n.</p>

<p style="text-align:justify"><span style="color:#000000">5. Agricultura:</span> En el cuadrante inferior derecho la figura representa la principal actividad agr&iacute;cola de la regi&oacute;n.</p>

<p style="text-align:justify">El escudo del Municipio ser&aacute; utilizado exclusivamente por los &oacute;rganos del Ayuntamiento, debi&eacute;ndose exhibir en forma ostensible en las oficinas y documentos oficiales, as&iacute; como en los bienes que integran el patrimonio municipal.</p>

<p style="text-align:justify">Cualquier uso que quiera d&aacute;rsele, deber&aacute; ser autorizado previamente por el Ayuntamiento.</p>

<p style="text-align:justify"><strong>Quien contravenga esta disposici&oacute;n se har&aacute; acreedor a las sanciones establecidas en este C&oacute;digo, sin perjuicio de las penas se&ntilde;aladas en la ley respectiva. </strong></p>

<p style="text-align:justify">Queda estrictamente prohibido el uso del escudo del Municipio para fines publicitarios no oficiales y de explotaci&oacute;n comercial.</p>

<p style="text-align:justify">En el Municipio de jes&uacute;s Mar&iacute;a son s&iacute;mbolos obligatorios la Bandera, el Himno y Escudo Nacionales, as&iacute; como el Escudo y el Himno del Estado de Aguascalientes.</p>

<p style="text-align:justify">El uso de estos s&iacute;mbolos se sujetar&aacute; a lo dispuesto por los ordenamientos federales y la Constituci&oacute;n Pol&iacute;tica para el Estado Libre y Soberano de Aguascalientes.</p>
