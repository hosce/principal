	<p  ><strong>CLIMA</strong></p>

	<p  >Jes&uacute;s Mar&iacute;a se localiza en la zona tropical norte y en la mayor parte del territorio es semiseco templado (89.72%) con una temperatura media anual de 17.9 &deg;C, siendo enero y diciembre los meses m&aacute;s fr&iacute;os y de mayo a julio los m&aacute;s calurosos.</p>

	<p  ><strong>FLORA</strong></p>

	<ul>
		<li  ><em>Agricultura</em>.- frijol, ma&iacute;z, avena, alfalfa, uvas y calabaza;</li>
		<li  ><em>Pastizal</em>.- zacate tres barbas, navajita, jarilla, &ldquo;engorda cabras&rdquo;, matorral subtropical, pastizal natural, pastizal inducido y chaparral;</li>
		<li  ><em>Bosques</em>.- encino, &aacute;lamo, mezquite, huizache y sauce.</li>
	</ul>

	<p  ><strong>FAUNA</strong></p>

	<p  >Lobo, zorra gris, coyote, tej&oacute;n, ardilla terrestre, tlacuache, conejo de cola blanca, venado cola blanca, liebre, codorniz y gansos silvestres. En la zona de Los Arquitos se encontraron vestigios de la rana de madriguera especie end&eacute;mica del estado y en peligro de extinci&oacute;n.&nbsp;</p>

	<p  ><strong>POBLACI</strong><strong>&Oacute;N</strong></p>

	<p  >La poblaci&oacute;n de Jes&uacute;s Mar&iacute;a es de 120 mil 405 habitantes, constituye 9.17% de la poblaci&oacute;n del estado de Aguascalientes.</p>

