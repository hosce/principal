	<p><strong>LOCALIZACI</strong><strong>&Oacute;N</strong></p>

	<p>El municipio de Jes&uacute;s Mar&iacute;a est&aacute; localizado en el estado de Aguascalientes, en la zona Centro Norte de M&eacute;xico. Forma parte de la Zona Metropolitana de Aguascalientes, con San Francisco de Los Romo y la capital.</p>

	<p>Tiene una altitud promedio de 1,880 msnm y su superficie representa el 8.89% del total del estado, con 493.1 km<sup>2</sup> (INEGI).</p>

